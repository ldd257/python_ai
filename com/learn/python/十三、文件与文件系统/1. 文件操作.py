f.close()
"""
文件对象方法
1. fileObject.close() 用于关闭一个已打开的文件。关闭后的文件不能再进行读写操作， 否则会触发 ValueError错误

1. fileObject.read([size]) 用于从文件读取指定的字符数，如果未给定或为负则读取所有。

1. fileObject.readline() 读取整行，包括 "\n" 字符。

1. fileObject.readlines() 用于读取所有行(直到结束符 EOF)并返回列表，该列表可以由 Python 的
for... in ... 结构进行处理
"""
f = open('将进酒.txt', 'r')
lines = f.readlines()
print(lines)
for each in lines:
 each.strip()
 print(each)
