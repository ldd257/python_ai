"""
打开文件
1. open(file, mode='r', buffering=None, encoding=None, errors=None, newline=None, closefd=True)
Open file and return a stream. Raise OSError upon failure.
a. file : 必需，文件路径（相对或者绝对路径）。
b. mode : 可选，文件打开模式
c. buffering : 设置缓冲
d. encoding : 一般使用utf8
e. errors : 报错级别
f. newline : 区分换行符

常见的 mode 如下表所示
打开模式 执行操作
'r' 以只读方式打开文件。文件的指针将会放在文件的开头。这是默认模式。
'w'
打开一个文件只用于写入。
如果该文件已存在则打开文件，并从开头开始编辑。
即原有内容会被删除。
如果该文件不存在，创建新文件。
'x' 写模式，新建一个文件，如果该文件已存在则会报错。
'a'
追加模式，打开一个文件用于追加。
如果该文件已存在，文件指针将会放在文件的结尾。
也就是说，新的内容将会被写入到已有内容之后。
如果该文件不存在，创建新文件进行写入。
'b' 以二进制模式打开文件。一般用于非文本文件，如：图片。
't' 以文本模式打开（默认）。一般用于文本文件，如：txt。
'+' 可读写模式（可添加到其它模式中使用）
"""

"""
打开一个文件，并返回文件对象，如果该文件无法被打开，会抛出 OSError 。
"""
f = open('将进酒.txt')
print(f)
# <_io.TextIOWrapper name='将进酒.txt' mode='r' encoding='cp936'>
